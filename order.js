function hide1() {
  let x = document.getElementById("p1");
  let y = document.getElementById("p11");

  if (x.style.display === "none") {
    x.style.display = "block";
    y.innerHTML="<b>Locksmith: Performer.</b> (Show less)"
  } else {
    y.innerHTML="<b>Locksmith: Performer.</b> (Show more)"
    x.style.display = "none";
  }
}

